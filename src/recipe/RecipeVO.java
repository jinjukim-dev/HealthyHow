package recipe;

public class RecipeVO {
	private int recipeNo;
	private int recipeCode;
	private String recipeTitle;
	private String recipeName;
	private String id;
	private String recipeIngredi;
	private String recipeSteps;
	private String recipeDate;
	private int recipeAvailable;
	private String fileName;
	
	public int getRecipeNo() {
		return recipeNo;
	}
	public void setRecipeNo(int recipeNo) {
		this.recipeNo = recipeNo;
	}
	public int getRecipeCode() {
		return recipeCode;
	}
	public void setRecipeCode(int recipeCode) {
		this.recipeCode = recipeCode;
	}
	public String getRecipeTitle() {
		return recipeTitle;
	}
	public void setRecipeTitle(String recipeTitle) {
		this.recipeTitle = recipeTitle;
	}
	public String getRecipeName() {
		return recipeName;
	}
	public void setRecipeName(String recipeName) {
		this.recipeName = recipeName;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRecipeIngredi() {
		return recipeIngredi;
	}
	public void setRecipeIngredi(String recipeIngredi) {
		this.recipeIngredi = recipeIngredi;
	}
	public String getRecipeSteps() {
		return recipeSteps;
	}
	public void setRecipeSteps(String recipeSteps) {
		this.recipeSteps = recipeSteps;
	}
	public String getRecipeDate() {
		return recipeDate;
	}
	public void setRecipeDate(String recipeDate) {
		this.recipeDate = recipeDate;
	}
	public int getRecipeAvailable() {
		return recipeAvailable;
	}
	public void setRecipeAvailable(int recipeAvailable) {
		this.recipeAvailable = recipeAvailable;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	
}
